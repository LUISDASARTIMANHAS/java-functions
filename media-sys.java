public class CalculoMedia {
    public static void main(String[] args) {
        // Definindo os três números
        double numero1 = 10.5;
        double numero2 = 8.2;
        double numero3 = 5.7;
        
        // Calculando a média
        double media = (numero1 + numero2 + numero3) / 3;
        
        // Exibindo o resultado
        System.out.println("A média dos números é: " + media);
    }
}
